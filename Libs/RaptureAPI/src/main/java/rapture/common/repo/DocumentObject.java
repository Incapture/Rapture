package rapture.common.repo;

import rapture.common.impl.jackson.JsonContent;

/**
 * A document object simply contains a json document
 * Note that this class is referenced in types.api - any changes to this file should be reflected there.
 * 
 * @author amkimian
 * 
 */
public class DocumentObject extends BaseObject {
    private JsonContent content;

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (!super.equals(obj)) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        DocumentObject other = (DocumentObject) obj;
        if (content == null) {
            if (other.content != null) {
                return false;
            }
        } else if (!content.equals(other.content)) {
            return false;
        }
        return true;
    }

    public JsonContent getContent() {
        return content;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((content == null) ? 0 : content.hashCode());
        return result;
    }

    public void setContent(JsonContent content) {
        this.content = content;
    }
}
